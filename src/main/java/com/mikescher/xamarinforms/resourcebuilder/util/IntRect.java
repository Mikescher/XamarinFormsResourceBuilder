package com.mikescher.xamarinforms.resourcebuilder.util;

public class IntRect
{
    public int X;
    public int Y;
    public int Width;
    public int Height;

    public IntRect(int x, int y, int w, int h)
    {
        X      = x;
        Y      = y;
        Width  = w;
        Height = h;
    }
}
